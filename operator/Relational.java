package operator;

public class Relational {
    public static void main(String args[]) {
        int a = 20;
        int b = 30;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        System.out.println("int");
    }
}
